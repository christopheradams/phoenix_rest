defmodule PhoenixRest do
  @moduledoc """
  This is the documentation for the PhoenixRest project.

  PhoenixRest makes the PlugRest library available for direct use in a Pheonix
  router, by adding a `resource` macro which dispatches requests to handler
  modules implementing REST semantics via a series of optional callbacks.

  For more information see:

    * [PlugRest](https://hexdocs.pm/plug_rest) - an Elixir port of Cowboy's
      REST sub-protocol for Plug applications
    * [Phoenix](https://hexdocs.pm/phoenix) - a productive web framework that
      does not compromise speed and maintainability.
  """
end
